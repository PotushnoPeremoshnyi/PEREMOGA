function incNum(n) {
  return n + 1;
}

const a = 5;
const b = incNum(a);
console.dir({ a, b }); 
//  { a: 5, b: 6 }
function incObj(obj) {
  obj.n += 1;
}

const obj = { n: 5 };
incObj(obj);
console.dir(obj); 
//  { n: 6 }
